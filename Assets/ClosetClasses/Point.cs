using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Point
{
    //class used to represent a lower-left-behind point
    public int x;
    public int y;
    public int z;

    public Point(int x, int y, int z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
